public class Loopy {
    public static void main(String[] args) {
        int x = 1;
        System.out.println("Value before start --->>>" + x);
        while(x<4) {
            System.out.println("Into loop ---->>>" + x);
            x = x + 1;
            System.out.println("Value is Equal "+ x);

        }
    }
}
